public class TiposAndOperadores {
    
    public static void main(String[] args) {
        
        //Em Java temos tipos primitivos
        byte oitob = 12; //8 bits
        short dezeb = 30000; //16 bits
        int inteiro = 209023940; // 32 bits
        long idadeTerra = 9000000000000000000L; // 64 bits

        //Decimais
        float flutuante = 1.45f; // 32 bits
        double duploflut = 1.45; //64 bits

        //Caracter
        char umaLetra = 'A';

        //Booleano
        boolean decisao = true; 

        //Operadores aritméticos
        int x = 10, y = 5;
        int soma = x + y;
        int subtracao = x - y;
        int multiplicacao = x * y;
        int divisao = x / y;
        int resto = x % y;  // Resto da divisão

        //Operadores relacionais
        int a = 10, b = 20;
        boolean igual = (a == b);
        boolean diferente = (a != b);
        boolean maiorQue = (a > b);
        boolean menorQue = (a < b);
        boolean maiorOuIgual = (a >= b);
        boolean menorOuIgual = (a <= b);

        //Operadores lógicos
        boolean c = true, d = false;
        boolean e = c && d;  // AND lógico
        boolean f = c || d;  // OR lógico
        boolean g = !c;      // NOT lógico




    }



}
